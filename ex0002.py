name = input('Digite algo:')
print('o tipo primitivo desse valor é', type(name))
print('é numérico?', name.isnumeric())
print('é alfabético?',name.isalpha())
print('é alphanuméricas?', name.isalnum())
print('Só tem epaços?',name.isspace())
print('está em letras maiúscula?', name.isupper())
print('está em letras maiúscula?', name.islower())
print('está capitalizada?', name.istitle())



